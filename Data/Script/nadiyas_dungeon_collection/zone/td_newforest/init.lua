---CURZONESCR.EnterSegment
--Engine callback function
function td_newforest.EnterSegment(zone, rescuing, segmentID, mapID)
  if rescuing ~= true then
    COMMON.BeginDungeon(zone.ID, segmentID, mapID)
  end
end

---CURZONESCR.ExitSegment
--Engine callback function
function td_newforest.ExitSegment(zone, result, rescue, segmentID, mapID)
  DEBUG.EnableDbgCoro() --Enable debugging this coroutine
  PrintInfo("=>> ExitSegment_td_newforest result "..tostring(result).." segment "..tostring(segmentID))
  
  --first check for rescue flag; if we're in rescue mode then take a different path
  COMMON.ExitDungeonMissionCheck(zone.ID, segmentID)
  if rescue == true then
    COMMON.EndRescue(zone, result, segmentID)
  elseif result ~= RogueEssence.Data.GameProgress.ResultType.Cleared then
    COMMON.EndDungeonDay(result, SV.checkpoint.Zone, SV.checkpoint.Segment, SV.checkpoint.Map, SV.checkpoint.Entry)
  else
    if segmentID == 0 then
	  if mapID == 6 then
	    SOUND:PlayFanfare("Fanfare/GoodNight")
		GAME:WaitFrames(280)
		UI:WaitShowDialogue(STRINGS:Format(RogueEssence.StringKey("EVENT_AFTER_BOSS_REST"):ToLocal()))
	    GAME:ContinueDungeon('td_newforest', 0, 7, 0, RogueEssence.Data.GameProgress.DungeonStakes.Risk, true, false)
	  else
        COMMON.EndDungeonDay(result, 'td_throwback_land', -1, 2, 2)
	  end
    else
      PrintInfo("No exit procedure found!")
      COMMON.EndDungeonDay(result, SV.checkpoint.Zone, SV.checkpoint.Segment, SV.checkpoint.Map, SV.checkpoint.Entry)
    end
  end
end

---CURZONESCR.Rescued
