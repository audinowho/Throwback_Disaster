-- Commonly included lua functions and data
require 'nadiyas_dungeon_collection.common'

-- Package name
local td_fieldgarden = {}

-------------------------------
-- Zone Callbacks
-------------------------------
---td_fieldgarden.Init
--Engine callback function
function td_fieldgarden.Init(zone)
  DEBUG.EnableDbgCoro() --Enable debugging this coroutine
  PrintInfo("=>> Init_td_fieldgarden")
end

---td_fieldgarden.EnterSegment
--Engine callback function
function td_fieldgarden.EnterSegment(zone, rescuing, segmentID, mapID)
  if rescuing ~= true then
    COMMON.BeginDungeon(zone.ID, segmentID, mapID)
  end
end

---td_fieldgarden.ExitSegment
--Engine callback function
function td_fieldgarden.ExitSegment(zone, result, rescue, segmentID, mapID)
  DEBUG.EnableDbgCoro() --Enable debugging this coroutine
  PrintInfo("=>> ExitSegment_td_fieldgarden result "..tostring(result).." segment "..tostring(segmentID))
  
  --first check for rescue flag; if we're in rescue mode then take a different path
  COMMON.ExitDungeonMissionCheck(zone.ID, segmentID)
  if rescue == true then
    COMMON.EndRescue(zone, result, segmentID)
  elseif result ~= RogueEssence.Data.GameProgress.ResultType.Cleared then
    COMMON.EndDungeonDay(result, SV.checkpoint.Zone, SV.checkpoint.Segment, SV.checkpoint.Map, SV.checkpoint.Entry)
  else
    if segmentID == 0 then
      COMMON.EndDungeonDay(result, 'td_throwback_land', -1, 2, 2)
    else
      PrintInfo("No exit procedure found!")
      COMMON.EndDungeonDay(result, SV.checkpoint.Zone, SV.checkpoint.Segment, SV.checkpoint.Map, SV.checkpoint.Entry)
    end
  end
end

---td_fieldgarden.Rescued
--Engine callback function
function td_fieldgarden.Rescued(zone, name, mail)
  COMMON.Rescued(zone, name, mail)
end

return td_fieldgarden

